import java.util.Scanner;

public class Text1018 {
    // 正方形周长
    public static void main(String[] args) {
        // 将Scanner实例化
        Scanner input = new Scanner(System.in);
        // 接收用户输入的边长
        int side = input.nextInt();
        // 输出打印边长乘4
        System.out.println(side * 4);
    }
}
